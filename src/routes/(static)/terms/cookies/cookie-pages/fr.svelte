<h2>Politique des cookies</h2>
<h3>1. Que sont les cookies ?</h3>
<p>
  Comme c'est la pratique courante pour la quasi-totalité des sites Web professionnels, ce site
  utilise des cookies et d'autres technologies de stockage de données de sites Web, telles que Web
  Storage et IndexedDB, ci-après également dénommées simplement &quot;cookies&quot;.
</p>
<p>
  Les cookies stockent de minuscules informations sur votre appareil, afin que notre site Web puisse
  s'en souvenir lors de votre prochaine visite. Cette page décrit les informations que nos cookies
  stockent, pourquoi nous en avons besoin et comment nous les utilisons.
</p>
<p>Nous vous indiquerons également comment vous pouvez empêcher l'enregistrement de ces cookies.</p>
<h3>2. Désactiver les cookies</h3>
<p>
  Vous pouvez empêcher le téléchargement des cookies en modifiant les paramètres de votre navigateur
  (voir le menu Aide de votre navigateur pour savoir comment procéder). Sachez que la désactivation
  des cookies peut affecter la fonctionnalité de ce site et de nombreux autres sites web que vous
  visitez. La désactivation des cookies pour notre site en particulier empêchera par exemple la
  possibilité de se connecter.
</p>
<p>
  Si vous vous préoccupez de votre vie privée en ligne, nous vous recommandons d'utiliser une
  extension de navigateur telle que
  <a
    href="https://www.google.com/url?q=https://addons.mozilla.org/en-US/firefox/addon/privacy-badger17/&amp;sa=D&amp;ust=1592214262287000"
    >Privacy Badger</a
  >
  (Firefox), <a href="https://www.ghostery.com/ghostery-ad-blocker">Ghostery</a> (disponible pour de
  nombreux navigateurs) ou <a href="https://ublockorigin.com/">uBlock Origin</a> (disponible pour de
  nombreux navigateurs).
</p>
