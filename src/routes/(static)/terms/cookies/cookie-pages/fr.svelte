<h3>4. Des analyses respectueuses de la vie privée</h3>
<p>
  Nous utilisons <a href="https://plausible.io/">Plausible</a>, un outil d'analyse axé sur la
  confidentialité, pour comprendre comment nos visiteurs utilisent
  <span class="notranslate">Welcome To My Garden</span>. Plausible nous permet de compter le nombre
  de visiteurs sur notre site web, de comprendre quelles sont les pages les plus visitées, d'où ils
  viennent, et quelles actions les gens entreprennent sur notre site.
</p>
<p>
  Contrairement à de nombreuses autres solutions d'analyse, Plausible
  <a href="https://plausible.io/privacy-focused-web-analytics">ne stocke pas de cookies</a> ou de données
  de site dans votre navigateur. Cela signifie que nous, ou notre fournisseur de services d'analyse,
  ne pouvons pas :
</p>
<ul>
  <li>vous suivre sur différents sites web</li>
  <li>vous suivre sur différents appareils</li>
  <li>vous suivre sur plusieurs jours (nous ne savons pas si vous revenez demain !).</li>
</ul>
<p>
  Pour en savoir plus sur la manière respectueuse de la vie privée dont Plausible recueille ses
  statistiques, lisez <a href="https://plausible.io/data-policy">leur politique de données</a>.
</p>
