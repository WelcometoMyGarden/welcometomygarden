<h2>Cookie-Richtlinie</h2>
<h3>1. Was sind Cookies</h3>
<p>
  Wie es bei fast allen professionellen Websites üblich ist, verwendet diese Seite Cookies und
  andere Technologien zur Speicherung von Website-Daten, wie Web Storage und IndexedDB, im Folgenden
  auch einfach als „Cookies“ bezeichnet.
</p>
<p>
  Cookies speichern kleine Informationsstücke auf Ihrem Gerät, sodass unsere Website sie beim
  nächsten Besuch wiedererkennen kann. Diese Seite beschreibt, welche Informationen unsere Cookies
  speichern, warum wir sie benötigen und wie wir sie verwenden.
</p>
<p>Wir werden auch erläutern, wie Sie verhindern können, dass diese Cookies gespeichert werden.</p>
<h3>2. Cookies deaktivieren</h3>
<p>
  Sie können verhindern, dass Cookies heruntergeladen werden, indem Sie Ihre Browsereinstellungen
  anpassen (siehe das Hilfemenü Ihres Browsers, um herauszufinden, wie dies funktioniert). Bitte
  beachten Sie, dass das Deaktivieren von Cookies die Funktionalität dieser und vieler anderer
  Websites, die Sie besuchen, beeinträchtigen könnte. Das Deaktivieren von Cookies auf unserer
  Website wird zum Beispiel die Möglichkeit deaktivieren, sich anzumelden.
</p>
<p>
  Wenn Sie sich um Ihre Online-Privatsphäre sorgen, empfehlen wir die Verwendung einer
  Browsererweiterung wie
  <a
    href="https://www.google.com/url?q=https://addons.mozilla.org/en-US/firefox/addon/privacy-badger17/&amp;sa=D&amp;ust=1592214262287000"
    >Privacy Badger</a
  >
  (Firefox), <a href="https://www.ghostery.com/ghostery-ad-blocker">Ghostery</a> (viele Browser)
  oder
  <a href="https://ublockorigin.com/">uBlock Origin</a> (viele Browser).
</p>
