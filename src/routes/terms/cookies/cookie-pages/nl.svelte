<h3>4. Privacyvriendelijke analyses</h3>
<p>
  We gebruiken <a href="https://plausible.io/">Plausible</a>, een op privacy gerichte analysetool,
  om te begrijpen hoe onze bezoekers Welcome To My Garden gebruiken. Plausible stelt ons in staat om
  anoniem het aantal bezoekers op onze website te tellen, te begrijpen welke pagina's het meest
  worden bezocht, waar ze vandaan komen en welke acties mensen ondernemen op onze site.
</p>
<p>
  In tegenstelling tot veel andere analytische oplossingen slaat Plausible
  <a href="https://plausible.io/privacy-focused-web-analytics">geen <em>enkele</em> cookie</a>
  lokale gegevens op in je browser. Dit betekent dat wij, of Plausi je
  <em>niet</em> kunnen volgen:
</p>
<ul>
  <li>over verschillende websites</li>
  <li>over verschillende apparaten</li>
  <li>
    over meerdere dagen (als je morgen terug komt, weten we niet dat jij dezelfde persoon bent!).
  </li>
</ul>
<p>
  Lees <a href="https://plausible.io/data-policy">hun gegevensbeleid</a> om meer te weten te komen over
  de privacyvriendelijke manier waarop Plausible statistieken verzamelt.
</p>
