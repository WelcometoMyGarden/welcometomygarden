<h2>Cookiebeleid</h2>
<h3>1. Wat zijn cookies?</h3>
<p>
  Bijna alle professionele websites, inclusief deze site, gebruiken cookies en andere technologieën
  voor het opslaan van webgegevens, zoals Web Storage en IndexedDB, ook wel &quot;cookies&quot;
  genoemd.
</p>
<p>
  Cookies bewaren kleine stukjes informatie op je apparaat, zodat onze website deze kan onthouden
  wanneer je onze site de volgende keer bezoekt. Op deze pagina leggen we uit welke informatie onze
  cookies opslaan, waarom we ze nodig hebben en hoe we ze gebruiken.
</p>
<p>We laten ook zien hoe je kunt voorkomen dat deze cookies worden opgeslagen.</p>
<h3>2. Cookies uitschakelen</h3>
<p>
  Je kunt voorkomen dat cookies worden gedownload door je browserinstellingen aan te passen (zie het
  Help-menu van je browser om erachter te komen hoe je dit kunt doen). Houd er rekening mee dat het
  uitschakelen van cookies invloed kan hebben op de functionaliteit van deze en vele andere websites
  die je bezoekt. Het uitschakelen van cookies voor onze site zal bijvoorbeeld de mogelijkheid tot
  inloggen uitschakelen.
</p>
<p>
  Als je je zorgen maakt over je online privacy, raden we het aan om een browserextensie zoals
  <a
    href="https://www.google.com/url?q=https://addons.mozilla.org/en-US/firefox/addon/privacy-badger17/&amp;sa=D&amp;ust=1592214262287000"
    >Privacy Badger</a
  >
  (Firefox), <a href="https://www.ghostery.com/ghostery-ad-blocker">Ghostery</a> (meerdere browsers)
  of <a href="https://ublockorigin.com/">uBlock Origin</a> (meerdere browsers) te gebruiken.
</p>
